import sys

# ------------------------------------------------------------
#  Високосный календарь – «перебор всех лет»
#  ------------------------------------------------------------
#  Здесь мы просто проходим по каждому году из диапазона [L, R]
#  и проверяем его «вручную» на високосность.
#  Такое решение работает только для небольших отрезков,
#  потому что его сложность O(R‑L+1) и при R‑L≈10^15 будет
#  полностью непрактичным (время и память).
# ------------------------------------------------------------

def is_leap(year: int) -> bool:
    """
    Возвращает True, если `year` – високосный.
    Правило: делится на 400   → високосный,
            иначе делится на 4 и не делится на 100 → високосный,
            иначе → обычный.
    """
    if year % 400 == 0:
        return True
    if year % 4 == 0 and year % 100 != 0:
        return True
    return False


def solve() -> None:
    data = sys.stdin.read().strip().split()
    if not data:
        return
    L, R = map(int, data)

    # Наивный подсчёт
    cnt = 0
    # Если диапазон слишком большой, программа «зависнет».
    # Мы оставляем её такой, чтобы показать принцип работы.
    for y in range(L, R + 1):
        if is_leap(y):
            cnt += 1

    sys.stdout.write(str(cnt))


if __name__ == "__main__":
    solve()
