import sys

# ------------------------------------------------------------
#  Високосный календарь
#  ------------------------------------------------------------
#  Год считается високосным, если
#      • делится на 400,  или
#      • делится на 4, но не делится на 100.
#  В задаче год 0 тоже подпадает под это правило.
# ------------------------------------------------------------

def leaps_up_to(x: int) -> int:
    """
    Возвращает количество високосных лет в диапазоне [0, x] (включительно).
    Для x < 0 возвращаем 0.
    """
    if x < 0:
        return 0
    # количество кратных 4, 100 и 400 в диапазоне [1, x]
    # (формула работает для положительных границ)
    cnt = x // 4 - x // 100 + x // 400
    # добавляем год 0, который тоже является високосным
    return cnt + 1


def solve() -> None:
    data = sys.stdin.read().strip().split()
    if not data:
        return
    L, R = map(int, data)

    # ответ = количество високосных лет в [0, R] минус количество в [0, L‑1]
    ans = leaps_up_to(R) - leaps_up_to(L - 1)
    sys.stdout.write(str(ans))


if __name__ == "__main__":
    solve()
